package modelo;

public class AlumnoMateriaVO {
	private int almat;
	private int aldni;
	private String codmat;
	private int nota;
	
	public int getAlmat() {
		return almat;
	}
	public void setAlmat(int almat) {
		this.almat = almat;
	}
	public int getAldni() {
		return aldni;
	}
	public void setAldni(int aldni) {
		this.aldni = aldni;
	}
	public String getCodmat() {
		return codmat;
	}
	public void setCodmat(String codmat) {
		this.codmat = codmat;
	}
	public int getNota() {
		return nota;
	}
	public void setNota(int nota) {
		this.nota = nota;
	}
	
	
}
